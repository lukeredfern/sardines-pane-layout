    public static int[] getColorSeq(int n){


        int l = 17;
        int t = 7*17;
        n = n % t;//22,25

        int i = n % l;//6,8
        int j = 7 - n / l;//1,1


        int[] matchColors = new int[2];

        matchColors[0] = colorArray[i][j];
        matchColors[1] = colorArray[i][j+1];

        return matchColors;
    }

    public static int getColor(int n, int m){
        return colorArray[n][m];
    }

    public static int getColorContainer(int SOURCE,int n) {
        switch (SOURCE) {
            case 0:
                return colorArray[8][n];
            case 1:
                return colorArray[1][n];
            case 2:
                return colorArray[15][n];
            case 3:
                return colorArray[6][n];
            default:
                return colorArray[16][n];
        }
    }

